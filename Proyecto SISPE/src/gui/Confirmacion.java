package gui;

public class Confirmacion {
	
	private String descripcion;
	private String archivoEnlace;
	private String mensajeEnlace;
	
	public Confirmacion() {

	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getArchivoEnlace() {
		return archivoEnlace;
	}

	public void setArchivoEnlace(String archivoEnlace) {
		this.archivoEnlace = archivoEnlace;
	}

	public String getMensajeEnlace() {
		return mensajeEnlace;
	}

	public void setMensajeEnlace(String mensajeEnlace) {
		this.mensajeEnlace = mensajeEnlace;
	}

}
